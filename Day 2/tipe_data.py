
# Hari 2 - Tipe Data

x = "Hello World"
print(x) #str

x = 20
print(x) #int

x = 20.5
print(x) #float

x = True
print(x) #bool

x = [1, 2, 3, 4, 5]
print(x) #list

x = {"name": "John", "age": 30}
print(x) #dict

x = (1, 2, 3, 4, 5)
print(x) #tuple

x = b"Hello World"
print(x) #bytes

x = None
print(x) #None

x = range(6)
print(x) #range object

x = 1j
print(x) #complex number

x = set([1, 2, 3, 4, 5])
print(x) #set

x = frozenset([1, 2, 3, 4, 5])
print(x) #frozenset
