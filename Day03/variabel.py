
# Hari 3 - Variabel

# Membuat variabel
nama = "Budi"
umur = 25
tinggi = 175.5

# Menampilkan nilai variabel
print("Nama:", nama)
print("Umur:", umur)
print("Tinggi:", tinggi)

# Mengubah nilai variabel
nama = "Andi"
umur = 30
print("\nSetelah diubah:")
print("Nama:", nama)
print("Umur:", umur)

# Multiple assignment
x, y, z = "Apel", "Jeruk", "Pisang"
print("\nMultiple assignment:")
print(x)
print(y)
print(z)

# Satu nilai untuk banyak variabel
a = b = c = "Python"
print("\nSatu nilai untuk banyak variabel:")
print(a)
print(b)
print(c)
